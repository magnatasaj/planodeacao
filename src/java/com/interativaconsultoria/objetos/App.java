package com.interativaconsultoria.objetos;

/**
 *
 * @author Administrador
 */
public class App {
    private int id_app;
    private String nome;
    private String nome_sistema;
    private String prefixo_tb;

    /**
     * @return the id_app
     */
    public int getId_app() {
        return id_app;
    }

    /**
     * @param id_app the id_app to set
     */
    public void setId_app(int id_app) {
        this.id_app = id_app;
    }

    /**
     * @return the nome
     */
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    public void setNome(String nome) {
        this.nome = nome;
    }

    /**
     * @return the nome_sistema
     */
    public String getNome_sistema() {
        return nome_sistema;
    }

    /**
     * @param nome_sistema the nome_sistema to set
     */
    public void setNome_sistema(String nome_sistema) {
        this.nome_sistema = nome_sistema;
    }

    /**
     * @return the prefixo_tb
     */
    public String getPrefixo_tb() {
        return prefixo_tb;
    }

    /**
     * @param prefixo_tb the prefixo_tb to set
     */
    public void setPrefixo_tb(String prefixo_tb) {
        this.prefixo_tb = prefixo_tb;
    }
    
}
